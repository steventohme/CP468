class City:
    """
    a class to represent a city instance

    ----------
    Attributes:
    ----------
    name (string): The name of the state
    description (string): The name of the city
    latitude (float): The latitude of the city
    longitude (float): The longitude of the city

    """
    # Constructor
    def __init__(self, name, description, latitude, longitude):
        self.name = name
        self.description = description
        self.latitude = latitude
        self.longitude = longitude



    
